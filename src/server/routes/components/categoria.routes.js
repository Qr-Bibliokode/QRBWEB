var client = require('swagger-client');

var urlSwaggerJson = 'http://localhost:3000/swagger/';

var swagger = new client({
    url: urlSwaggerJson,
    success: function () {
    }
});

module.exports = [
    {
        method: 'GET',
        path: '/categorias/',
        handler: function (request, reply) {
            swagger.categoria.list({max: 10, offset: 0}, function (response) {
                reply(response.data).type('application/json')
            }, function (response) {
                reply(response.data).code(500).type('application/json')
            });
        }
    },
    {
        method: 'GET',
        path: '/categorias/{categoriaId}',
        handler: function (request, reply) {
            swagger.categoria.getById({categoriaId: request.params.categoriaId}, function (response) {
                reply(response.data).type('application/json')
            }, function (response) {
                reply(response.data).code(500).type('application/json')
            });
        }
    },
    {
        method: 'DELETE',
        path: '/categorias/{id}',
        handler: function (request, reply) {
            swagger.categoria.delete({categoriaId: request.params.id}, function (response) {
                reply(response.data).type('application/json')
            }, function (response) {
                reply(response.data).code(500).type('application/json')
            });
        }
    },
    {
        method: 'POST',
        path: '/categorias/',
        handler: function (request, reply) {
            swagger.categoria.create({categoria: request.payload}, function (response) {
                reply(response.data).type('application/json')
            }, function (response) {
                reply(response.data).code(500).type('application/json')
            });
        }
    },
    {
        method: 'PUT',
        path: '/categorias/{id}',
        handler: function (request, reply) {
            swagger.categoria.update({categoria: request.payload, categoriaId: request.params.id}, function (response) {
                reply(response.data).type('application/json')
            }, function (response) {
                reply(response.data).code(500).type('application/json')
            });
        }
    }
];